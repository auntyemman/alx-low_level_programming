/**
 * _abs - A function that computes the absolute value of an integer
 *
 * Return: absolute value of integer
 * @n: int is an integer
 *
 */

int _abs(int n)
{

        if (n < 0)
        {
                return (-n);
        }

        else if (n > 0)
        {
                return (n);
        }

        else
        {
                return (n);
        }
}
